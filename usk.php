                <img class="avatar" src="assets/images/usk/agnes_goyet.jpg" alt="">
                <div class="description">Agnès découvre le bonheur de dessiner dehors lorsqu’elle devient parisienne. Paris est son sujet de prédilection, elle aime s’installer aux terrasses des bistrots, se laisser imprégner des ambiances comme pour arrêter le temps. Elle transporte ses carnets partout, au coin de sa rue ou au bout du monde. Elle a choisi l’aquarelle, technique légère et spontanée qui s’adapte parfaitement aux carnets de voyage. Croquer sur le vif est pour elle le meilleur moyen d’être dans la vraie vie et toujours l’occasion de belles surprises. Elle est membre et correspondante des Urban Sketchers parisiens depuis 2018.</div>
