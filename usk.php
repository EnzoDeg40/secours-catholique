            <div class="item">
                <div class="username">Mat Let</div>
                <img class="avatar" src="assets/images/usk/mat_let.jpg" alt="">
                <div class="description">Dessinateur autodidacte, Mat s’est lancé dans le dessin à plein temps en 2017. Grand voyageur, il a raconté dans ses carnets ses aventures en Iran, au Cameroun ou au Brésil avec humour et l’envie de se connecter aux gens et aux lieux qui croisent sa route. C’est cette même route qui l’a ramené chez lui, à Paris, où il a fini par prendre conscience que la magie du voyage n’a rien à voir avec la distance parcourue mais plutôt avec le regard que l’on porte sur ce qui nous entoure. On le retrouve donc dans la capitale, un crayon à la main, le carnet dans l’autre en train de croquer tel un reporter de la vie quotidienne dans le métro, dans les bars où dans les parcs, à l’affut de l’incongru, de l’imprévu et de l’humanité qui s’y déroule.</div>
