                <div class="username">Claire Archenault</div>
                <img class="avatar" src="assets/images/usk/claire_archenault.jpg" alt="">
                <div class="description">Claire Archenault est peintre et graveur et réalise des carnets au fil des jours. Son parcours professionnel a été costumière et décoratrice dans le théâtre et le music-hall, roughman pour la pub, puis peintre en décors. Elle a aussi réalisé des copies d’oeuvres de maître au musée du Louvre. Si l’univers de l’estampe est devenu ces dernières années un mode d’expression et de transmission important, la peinture reste son langage favori, et l’aquarelle un médium de prédilection pour le travail en plein air. Elle est membre de plusieurs associations d’artistes depuis 20 ans et de UrbanSketchers Paris depuis 2015.</div>
